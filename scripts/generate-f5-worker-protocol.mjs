import { readFile, writeFile, mkdir } from "node:fs/promises";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const schemaPath = path.join(root, "config", "protocols", "f5-worker.schema.json");
const tsPath = path.join(root, "src", "pipeline", "generated", "f5-worker-protocol.ts");
const pythonPath = path.join(root, "scripts", "generated", "f5_worker_protocol.py");
const schema = JSON.parse(await readFile(schemaPath, "utf8"));

function zodExpression(value) {
  if (Object.hasOwn(value, "const")) return `z.literal(${JSON.stringify(value.const)})`;
  if (value.enum) return `z.enum(${JSON.stringify(value.enum)})`;
  if (value.oneOf) return `z.union([${value.oneOf.map(zodExpression).join(", ")}])`;
  if (value.type === "string") {
    let result = "z.string()";
    if (value.minLength !== undefined && value.minLength === value.maxLength) result += `.length(${value.minLength})`;
    else if (value.minLength !== undefined) result += `.min(${value.minLength})`;
    return result;
  }
  if (value.type === "integer") {
    let result = "z.number().int()";
    if (value.minimum === 0) result += ".nonnegative()";
    else if (value.minimum === 1) result += ".positive()";
    return result;
  }
  if (value.type === "number") {
    let result = "z.number()";
    if (value.minimum === 0) result += ".nonnegative()";
    if (value.exclusiveMinimum === 0) result += ".positive()";
    return result;
  }
  if (value.type === "boolean") return "z.boolean()";
  if (value.type === "null") return "z.null()";
  if (value.type === "object") {
    const required = new Set(value.required ?? []);
    const fields = Object.entries(value.properties ?? {}).map(([name, child]) => `  ${JSON.stringify(name)}: ${zodExpression(child)}${required.has(name) ? "" : ".optional()"},`).join("\n");
    return `z.object({\n${fields}\n})${value.additionalProperties === false ? ".strict()" : ""}`;
  }
  throw new Error(`Unsupported JSON Schema fragment: ${JSON.stringify(value)}`);
}

function pythonType(value) {
  if (Object.hasOwn(value, "const")) return `Literal[${JSON.stringify(value.const)}]`;
  if (value.enum) return `Literal[${value.enum.map((item) => JSON.stringify(item)).join(", ")}]`;
  if (value.oneOf) return `Union[${value.oneOf.map(pythonType).join(", ")}]`;
  if (value.type === "string") return "str";
  if (value.type === "integer") return "int";
  if (value.type === "number") return "float";
  if (value.type === "boolean") return "bool";
  if (value.type === "null") return "None";
  throw new Error(`Unsupported Python type fragment: ${JSON.stringify(value)}`);
}

const definitions = Object.entries(schema.$defs);
const schemaNames = Object.fromEntries(definitions.map(([name]) => [name, `${name[0].toLowerCase()}${name.slice(1)}Schema`]));
const ts = [
  "// Generated by scripts/generate-f5-worker-protocol.mjs. Do not edit.",
  'import { z } from "zod";',
  "",
  ...definitions.flatMap(([name, definition]) => [`export const ${schemaNames[name]} = ${zodExpression(definition)};`, `export type ${name} = z.infer<typeof ${schemaNames[name]}>;`, ""]),
  `export const f5WorkerInboundSchema = z.union([${schemaNames.F5WorkerRequest}, ${schemaNames.F5WorkerShutdownRequest}]);`,
  `export const f5WorkerOutboundSchema = z.union([${schemaNames.F5WorkerReady}, ${schemaNames.F5WorkerResult}, ${schemaNames.F5WorkerShutdownResult}]);`,
  "export type F5WorkerInbound = z.infer<typeof f5WorkerInboundSchema>;",
  "export type F5WorkerOutbound = z.infer<typeof f5WorkerOutboundSchema>;",
  "",
].join("\n");

const pythonClasses = definitions.map(([name, definition]) => {
  const required = new Set(definition.required ?? []);
  const fields = Object.entries(definition.properties ?? {}).map(([field, value]) => `    ${field}: ${required.has(field) ? pythonType(value) : `NotRequired[${pythonType(value)}]`}`).join("\n");
  return `class ${name}(TypedDict):\n${fields || "    pass"}`;
}).join("\n\n");
const python = [
  "# Generated by scripts/generate-f5-worker-protocol.mjs. Do not edit.",
  "from typing import Literal, Union",
  "from typing_extensions import NotRequired, TypedDict",
  "",
  pythonClasses,
  "",
  "F5WorkerInbound = Union[F5WorkerRequest, F5WorkerShutdownRequest]",
  "F5WorkerOutbound = Union[F5WorkerReady, F5WorkerResult, F5WorkerShutdownResult]",
  "",
].join("\n");

async function writeOrCheck(filePath, content) {
  if (process.argv.includes("--check")) {
    const current = await readFile(filePath, "utf8").catch(() => "");
    if (current !== content) throw new Error(`${path.relative(root, filePath)} is stale. Run npm run generate:protocols.`);
    return;
  }
  await mkdir(path.dirname(filePath), { recursive: true });
  await writeFile(filePath, content, "utf8");
}

await Promise.all([writeOrCheck(tsPath, ts), writeOrCheck(pythonPath, python)]);
